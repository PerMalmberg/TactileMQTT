// A HTML reader
function HTMLReader() {
	"use strict";

	var myIsValid = false;
	var myUrl = null;
	var myData = null;

	this.Read = function( url )
	{
		var jqxhr = $.ajax(
			{
				cache: false,
				dataType: "html",
				url: url,
				async: false,
				error: OnError,
				success: OnSuccess
			}
		 );

		return myIsValid;
	}

	///////////////////////////////////////////////////////////////////////////////////
	//
	//
	///////////////////////////////////////////////////////////////////////////////////
	this.Get = function()
	{
		return myData;
	}

	///////////////////////////////////////////////////////////////////////////////////
	//
	//
	///////////////////////////////////////////////////////////////////////////////////
	var OnError = function( jqXHR, textStatus, errorThrown )
	{
		console.log( "Failed to retrieve data: " + errorThrown );
	}

	///////////////////////////////////////////////////////////////////////////////////
	//
	//
	///////////////////////////////////////////////////////////////////////////////////
	var OnSuccess = function( data, textStatus, jqXHR )
	{
		myData = data;
		myIsValid = true;
	}
}